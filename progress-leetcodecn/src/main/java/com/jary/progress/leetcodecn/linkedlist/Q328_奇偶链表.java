package com.jary.progress.leetcodecn.linkedlist;

/**
 * https://leetcode-cn.com/problems/odd-even-linked-list/
 */
public class Q328_奇偶链表 {

    /**
     * 1->2->3->4->5
     * @param head
     * @return
     */
    public ListNode oddEvenList(ListNode head) {
        if(head == null || head.next == null) {
            return head;
        }

        return null;
    }

}
